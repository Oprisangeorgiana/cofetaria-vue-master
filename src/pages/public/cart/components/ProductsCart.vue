<template>
  <div>
    <one-product-cart v-for="currentCartProduct in orderProducts"
                      :key="currentCartProduct.product_id"
                      :cart-product="currentCartProduct"/>
  </div>
</template>

<script>


  import oneProductCart from 'pages/public/cart/components/OneProductCart'

  export default {
    //  ================================================================================
    //  ========================   GLOBAL AWARENESS   ==================================
    //  ================================================================================
    name: 'products-cart',

    parent: {},
    //  ================================================================================
    //  ========================   CUSTOM MIDDLEWARE   =================================
    //  ================================================================================
    layout: 'PublicPortal',
    //  ================================================================================
    //  ==========================   DEPENDENCIES   ====================================
    //  ================================================================================
    components: {
      oneProductCart
    },
    directives: {},
    filters: {},
    //  ================================================================================
    //  ===========================   COMPOSITION   ====================================
    //  ================================================================================
    mixins: [],
    extends: {},
    //  ================================================================================
    //  ========================   COMPONENT INTERFACE   ===============================
    //  ================================================================================
    inheritAttrs: {},
    model: {},
    props: {
      cartProductsList: null
    },
    //  ================================================================================
    //  ============================   LOCAL STATE   ===================================
    //  ================================================================================
    data: function () {
      return {}
    },
    computed: {
      orderProducts(){
        if(this.cartProductsList === null)
          return null
        return this.cartProductsList.orderProducts
      }
    },
    //  ================================================================================
    //  ========================   NON-REACTIVE PROPERTIES   ===========================
    //  ================================================================================
    methods: {},
  }
</script>

